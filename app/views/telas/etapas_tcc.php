                    <strong>Preliminares:</strong> A estrutura inicial do TCC inclui a capa, folha de rosto, sumário e outras informações iniciais que ajudam a organizar o trabalho. <br>
                    <strong>Introdução:</strong> Apresentação do tema, do problema de pesquisa, dos objetivos e da justificativa do estudo. A introdução contextualiza o leitor e o prepara para o que será abordado no trabalho.<br>
                    <strong>Revisão de Literatura:</strong> Aqui, o pesquisador apresenta as principais teorias, estudos e autores que já abordaram o tema. A revisão de literatura é crucial para fundamentar o trabalho e demonstrar o conhecimento existente sobre o assunto.<br>
                    <strong>Metodologia:</strong> Detalhamento dos procedimentos de pesquisa que serão seguidos, como métodos de coleta e análise de dados.<br>
                    <strong>Desenvolvimento:</strong> A seção principal do TCC, onde são apresentados os dados obtidos, as análises realizadas e os resultados encontrados. O desenvolvimento é o corpo do trabalho, onde a pesquisa é efetivamente discutida.<br>
                    <strong>Conclusão:</strong> A conclusão resume os principais achados da pesquisa, responde ao problema de pesquisa e sugere possíveis caminhos para futuras investigações.<br>
                    <strong>Referências:</strong> Lista de todas as fontes consultadas e utilizadas no trabalho. As referências devem ser apresentadas conforme as normas da instituição.
